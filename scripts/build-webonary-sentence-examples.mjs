import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const rootDir = process.cwd();
const parsedPath = path.join(rootDir, 'docs/plan/source/webonary-semai-parsed.json');
const outPath = path.join(
  rootDir,
  'supabase/functions/_shared/webonarySentenceExamples.generated.ts',
);

const normalizeText = (value) =>
  String(value ?? '')
    .trim()
    .replace(/\s+/g, ' ');

const normalizeComparable = (value) => normalizeText(value).toLowerCase();

const makeSlug = (value) =>
  normalizeComparable(value)
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');

const build = async () => {
  const raw = JSON.parse(await readFile(parsedPath, 'utf8'));
  if (!Array.isArray(raw)) {
    throw new Error('Parsed Webonary file must be an array.');
  }

  const rows = [];
  const seen = new Set();

  for (const entry of raw) {
    const headword = normalizeText(entry?.word);
    const senses = Array.isArray(entry?.senses) ? entry.senses : [];

    for (const sense of senses) {
      const examples = Array.isArray(sense?.examples) ? sense.examples : [];
      for (const example of examples) {
        const semai = normalizeText(example?.semai);
        const ms = normalizeText(example?.ms);
        const en = normalizeText(example?.en);

        if (!semai || (!ms && !en)) {
          continue;
        }

        const dedupeKey = [
          normalizeComparable(semai),
          normalizeComparable(ms),
          normalizeComparable(en),
        ].join('|');
        if (seen.has(dedupeKey)) {
          continue;
        }
        seen.add(dedupeKey);

        const idCore = `${makeSlug(headword || semai)}-${makeSlug(semai).slice(0, 40)}`;
        const fallbackId = `example-${rows.length + 1}`;

        rows.push({
          id: idCore || fallbackId,
          semai,
          ms,
          en,
          headword: normalizeComparable(headword),
          source: 'Webonary Cloud API sentence example',
        });
      }
    }
  }

  rows.sort((a, b) => a.semai.localeCompare(b.semai));

  const file = [
    '// Auto-generated by scripts/build-webonary-sentence-examples.mjs',
    `// Source: ${path.relative(rootDir, parsedPath)}`,
    '',
    "import type { SentenceExampleEntry } from './translationGlossary.ts';",
    '',
    'export const WEBONARY_SENTENCE_EXAMPLES: SentenceExampleEntry[] = ',
    `${JSON.stringify(rows, null, 2)} as SentenceExampleEntry[];`,
    '',
  ].join('\n');

  await writeFile(outPath, file, 'utf8');
  console.log(`Wrote ${rows.length} sentence examples to ${outPath}`);
};

build().catch((error) => {
  console.error(error);
  process.exit(1);
});
