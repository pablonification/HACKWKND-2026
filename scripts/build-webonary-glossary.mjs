import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const rootDir = process.cwd();
const parsedPath = path.join(rootDir, 'docs/plan/source/webonary-semai-parsed.json');
const outPath = path.join(rootDir, 'supabase/functions/_shared/webonaryGlossary.generated.ts');

const splitPrimaryDefinition = (value) => {
  const normalized = String(value ?? '')
    .trim()
    .replace(/\s+/g, ' ');
  if (!normalized) {
    return '';
  }

  return normalized
    .split(';')[0]
    .split(',')[0]
    .replace(/^to\s+/i, '')
    .trim();
};

const normalizeToken = (value) =>
  String(value ?? '')
    .trim()
    .replace(/\s+/g, ' ')
    .toLowerCase();

const makeSlug = (value) =>
  normalizeToken(value)
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');

const build = async () => {
  const raw = JSON.parse(await readFile(parsedPath, 'utf8'));
  if (!Array.isArray(raw)) {
    throw new Error('Parsed Webonary file must be an array.');
  }

  const rows = [];
  const seen = new Set();

  for (const entry of raw) {
    const semai = normalizeToken(entry?.word);
    if (!semai) {
      continue;
    }

    const senses = Array.isArray(entry?.senses) ? entry.senses : [];
    const bestSense = senses.find(
      (sense) =>
        splitPrimaryDefinition(sense?.definition_en) &&
        splitPrimaryDefinition(sense?.definition_ms),
    );

    if (!bestSense) {
      continue;
    }

    const en = normalizeToken(splitPrimaryDefinition(bestSense.definition_en));
    const ms = normalizeToken(splitPrimaryDefinition(bestSense.definition_ms));
    if (!en || !ms) {
      continue;
    }

    const dedupeKey = `${semai}|${ms}|${en}`;
    if (seen.has(dedupeKey)) {
      continue;
    }
    seen.add(dedupeKey);

    const idCore = `${makeSlug(semai)}-${makeSlug(en)}`;
    const fallbackId = `entry-${rows.length + 1}`;

    rows.push({
      id: makeSlug(idCore) || fallbackId,
      semai,
      ms,
      en,
      category: normalizeToken(entry?.pos_ms || 'webonary'),
      source: 'Webonary Cloud API',
    });
  }

  rows.sort((a, b) => a.semai.localeCompare(b.semai));

  const file = [
    '// Auto-generated by scripts/build-webonary-glossary.mjs',
    `// Source: ${path.relative(rootDir, parsedPath)}`,
    '',
    "import type { GlossaryEntry } from './translationGlossary.ts';",
    '',
    'export const WEBONARY_GLOSSARY: GlossaryEntry[] = ',
    `${JSON.stringify(rows, null, 2)} as GlossaryEntry[];`,
    '',
  ].join('\n');

  await writeFile(outPath, file, 'utf8');
  console.log(`Wrote ${rows.length} entries to ${outPath}`);
};

build().catch((error) => {
  console.error(error);
  process.exit(1);
});
